package org.atlasapi.system.legacy;

public class UnresolvedLegacySegmentException extends Exception {

    public UnresolvedLegacySegmentException() {
        super();
    }

    public UnresolvedLegacySegmentException(String message, Throwable cause) {
        super(message, cause);
    }

    public UnresolvedLegacySegmentException(String message) {
        super(message);
    }

    public UnresolvedLegacySegmentException(Throwable cause) {
        super(cause);
    }

}
